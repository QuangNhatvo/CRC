#include "rtu_crc.h"
#include <stdio.h>

int main() {
    unsigned char data[] = {0xCF, 0x00, 0x00, 0x01, 0x12, 0x00, 0xFD, 0xEE, 0x01, 0x00, 0x0C, 0x15, 0xBB, 0x17, 0x20, 0x68, 0x88, 0x09, 0x38, 0x2D, 0x33, 0x52, 0x49};
    int length = sizeof(data) / sizeof(data[0]);
    unsigned short result = crc16(data, length);
    printf("CRC result: 0x%X\n", result);
    return 0;
}
